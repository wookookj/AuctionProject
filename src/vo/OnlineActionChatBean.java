package vo;

public class OnlineActionChatBean {
	private int action_num;
	private String id;
	private String msg;
	
	public int getAction_num() {
		return action_num;
	}
	public void setAction_num(int action_num) {
		this.action_num = action_num;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	
	
}
