package Action_projcet;

public class Signup_DTO {
	private String id;
	private String sign_pw;
	private String sign_name;
	private String sign_tell;
	private String sign_email;
	private String sign_addr;
	private String sign_birth;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSign_pw() {
		return sign_pw;
	}
	public void setSign_pw(String sign_pw) {
		this.sign_pw = sign_pw;
	}
	public String getSign_name() {
		return sign_name;
	}
	public void setSign_name(String sign_name) {
		this.sign_name = sign_name;
	}
	public String getSign_tell() {
		return sign_tell;
	}
	public void setSign_tell(String sign_tell) {
		this.sign_tell = sign_tell;
	}
	public String getSign_email() {
		return sign_email;
	}
	public void setSign_email(String sign_email) {
		this.sign_email = sign_email;
	}
	public String getSign_addr() {
		return sign_addr;
	}
	public void setSign_addr(String sign_addr) {
		this.sign_addr = sign_addr;
	}
	public String getSign_birth() {
		return sign_birth;
	}
	public void setSign_birth(String sign_birth) {
		this.sign_birth = sign_birth;
	}
	
	
}
